  end

  def search 
    @matched_services = Service.ransack(params[:a]).result(distinct: true)
    @matched_providers = Director.ransack(params[:a]).result(distinct: true) 

    render json: {matched_services: [], matched_providers: []}
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_provider
      @provider = Provider.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def provider_params
      params.require(:provider).permit(:name, :location, services_attributes: [:name, :description, :cost, :capacity])
    end
end
